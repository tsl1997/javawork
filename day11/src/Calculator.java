/**这是一个计算器类*/
public class Calculator {
	/**加法 c是返回的int结果
	 * @param a 第一个元素
	 * @param b 第二个元素
	 * */
	public int add(int a,int b){
		int c=a+b;
		return c;
	}
	/**减法 c是返回的int结果
	 * @param a 第一个减数
	 * @param b 第二个被减数
	 * */
	public int minus(int a,int b){
		int c=a-b;
		return c;
	}
	/**乘法 c是返回的int结果
	 * @param a 第一个元素
	 * @param b 第二个元素
	 * */
	public int multiple(int a,int b){
		int c=a*b;
		return c;
	}
	/**除法 c是返回的double结果
	 * @param a 第一个元素
	 * @param b 第二个元素
	 * */
	public double divide(int a,double b){
		double c=a/b;
		return c;
	}




}
